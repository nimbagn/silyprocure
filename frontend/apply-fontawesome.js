// Fonction pour ajouter Font Awesome si absent
function addFontAwesome(content) {
    const fontAwesomeLink = '<link rel="stylesheet" href="https://cdnjs.cloudflare.com/ajax/libs/font-awesome/6.4.0/css/all.min.css" integrity="sha512-iecdLmaskl7CVkqkXNQ/ZH/XLlvWZOJyj7Yy7tcenmpD1ypASozpmT/E0iPtmFIB46ZmdtAc9eNBvH0H/ZpiBw==" crossorigin="anonymous" referrerpolicy="no-referrer" />';
    
    // VÃ©rifier si Font Awesome est dÃ©jÃ  prÃ©sent
    if (content.includes('font-awesome') || content.includes('fontawesome') || content.includes('all.min.css')) {
        return content;
    }
    
    // Trouver la position aprÃ¨s le dernier <link> dans <head>
    const headMatch = content.match(/<head[^>]*>([\s\S]*?)<\/head>/i);
    if (headMatch) {
        const headContent = headMatch[1];
        // InsÃ©rer aprÃ¨s le dernier <link> ou avant </head>
        const lastLinkMatch = headContent.match(/(<link[^>]*>[\s\S]*?)(?=<link|$)/);
        if (lastLinkMatch) {
            return content.replace(
                lastLinkMatch[0],
                lastLinkMatch[0] + '\n    ' + fontAwesomeLink
            );
        } else {
            return content.replace('</head>', '    ' + fontAwesomeLink + '\n</head>');
        }
    }
    
    return content;
}

